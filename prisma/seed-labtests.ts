async function main() {
  console.log('🧪 Seeding lab tests...')

  for (const test of labTests) {
    const t = test as any

    // Конвертируем числовые нормы (старый формат) в текстовые строки (новый формат)
    const unit = t.unit ?? null
    const normMale = t.normMale
      ? t.normMale
      : (t.normMaleMin != null && t.normMaleMax != null)
        ? `${t.normMaleMin}–${t.normMaleMax}${unit ? ' ' + unit : ''}`
        : null
    const normFemale = t.normFemale
      ? t.normFemale
      : (t.normFemaleMin != null && t.normFemaleMax != null)
        ? `${t.normFemaleMin}–${t.normFemaleMax}${unit ? ' ' + unit : ''}`
        : null
    const normGeneral = t.normGeneral
      ? t.normGeneral
      : (t.normMin != null && t.normMax != null)
        ? `${t.normMin}–${t.normMax}${unit ? ' ' + unit : ''}`
        : null
    // normNote → добавляем к normGeneral или normMale
    const noteStr = t.normNote ? ` (${t.normNote})` : ''

    const data = {
      title: test.title,
      description: test.description,
      category: test.category,
      unit,
      normMale: normMale ? normMale + (normMale === t.normMale ? '' : noteStr) : null,
      normFemale: normFemale || null,
      normGeneral: normGeneral ? normGeneral + (normGeneral === t.normGeneral ? '' : noteStr) : null,
      preparation: t.preparation ?? null,
    }

    await prisma.labTest.upsert({
      where: { slug: test.slug },
      update: data,
      create: { slug: test.slug, ...data },
    })
    console.log(`  ✓ ${test.title}`)
  }

  console.log(`\n✅ Done: ${labTests.length} lab tests seeded.`)
}

main()
  .catch(e => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
